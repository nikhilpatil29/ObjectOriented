class Appointment():


    def set_doctor(self,set_doctor):
        self.__doctor = set_doctor

    def set_patient(self, set_patient):
        self.__patient = set_patient

    def set_date(self, date):
        self.__date = date

    def get_doctor(self):
        return self.__doctor

    def get_patient(self):
        return self.__patient

    def get_date(self):
        return self.__date



